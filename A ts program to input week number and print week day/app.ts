var week: any = prompt("please enter the day number")
console.log(week);
var alpha:number=Number(week)




if (alpha == 1) {
    console.log("Day Is Monday");
} else if (alpha == 2) {
    console.log("Day Is Tuesday");
} else if (alpha == 3) {
    console.log("Day Is Wednesday");
}
else if (alpha == 4) {
    console.log("Day Is Thursday");
}
else if (alpha == 5) {
    console.log("Day Is Friday");
}
else if (alpha == 6) {
    console.log("Day Is Saturday");
}
else if (alpha == 7) {
    console.log("Day Is Sunday");
}
else {
    console.log("ERROR");
}